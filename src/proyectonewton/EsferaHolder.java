public class EsferaHolder {
    private int valor;
    
    /**
     *
     */
    public EsferaHolder(){
        valor=1;
    }
    
    /**
     *
     * @param valor : valor a cambiar de numero de esferas
     */
    public void setValor(int valor){
        this.valor=valor;
    }
    
    /**
     *
     * @return :retorna el numero de esferas
     */
    public int getValor(){
        return valor;
    }
    
    
}
